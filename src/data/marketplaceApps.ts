// Helper to get apps by category
export function getAppsByCategory(category: AppCategory): MarketplaceApp[] {
  return marketplaceApps.filter(app => app.category === category);
}

// Helper to get featured apps
export function getFeaturedApps(): MarketplaceApp[] {
  return marketplaceApps.filter(app => app.featured);
}

// Helper to get new apps
export function getNewApps(): MarketplaceApp[] {
  return marketplaceApps.filter(app => app.new);
}

// Helper to search apps
export function searchApps(query: string): MarketplaceApp[] {
  const lowerQuery = query.toLowerCase();
  return marketplaceApps.filter(app =>
    app.name.toLowerCase().includes(lowerQuery) ||
    app.description.toLowerCase().includes(lowerQuery) ||
    app.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
}
